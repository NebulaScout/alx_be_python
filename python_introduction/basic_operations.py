# Basic Arithmetic operations

number1 = 10
number2 = 5

# Addition
result = number1 + number2

print("Addition of", number1, "and", number2, "is:", result)

# Subtraction
result = number1 - number2
print("Subtraction of", number1, "and", number2, "is:", result)

# Multiplication
result = number1 * number2
print("Multiplication of", number1, "and", number2, "is:", result)

